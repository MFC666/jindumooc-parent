package com.jindumooc.vojo.user;

import java.io.Serializable;

public class UserApproval implements Serializable {

    //用于储存申请实名认证的信息

    private int userId;
    private int id;
    private String idcard;
    private String faceImg;
    private String backImg;
    private String truename;
    private String note;
    private String status;
    private int operatorId;
    private String createdTime;
    private int totalApproval;
    private String nickName;

    public String getNickName() {
        return nickName;
    }

    public void setNickName(String nickName) {
        this.nickName = nickName;
    }

    public int getTotalApproval() {
        return totalApproval;
    }

    public void setTotalApproval(int totalApproval) {
        this.totalApproval = totalApproval;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getIdCard() {
        return idcard;
    }

    public void setIdCard(String idCard) {
        this.idcard = idCard;
    }

    public String getFaceImg() {
        return faceImg;
    }

    public void setFaceImg(String faceImg) {
        this.faceImg = faceImg;
    }

    public String getBackImg() {
        return backImg;
    }

    public void setBackImg(String backImg) {
        this.backImg = backImg;
    }

    public String getTrueName() {
        return truename;
    }

    public void setTrueName(String trueName) {
        this.truename = trueName;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public int getOperatorId() {
        return operatorId;
    }

    public void setOperatorId(int operatorId) {
        this.operatorId = operatorId;
    }

    public String getCreatedTime() {
        return createdTime;
    }

    public void setCreatedTime(String createdTime) {
        this.createdTime = createdTime;
    }
}
